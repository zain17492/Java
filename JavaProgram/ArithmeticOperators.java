package course;
public class ArithmeticOperators {
	public static void main(String[] args) {
		
		int x = 50;
		int y = 30;
		int sum;
		
		
		System.out.println("Num 1 ="+ x);
		System.out.println("Num 2 =" +y);
	
		sum=x+y;
		System.out.println("50+30:-" +sum);
		
		sum=x*y;
		System.out.println("50*30:-" +sum);
		
		sum=x-y;
		System.out.println("50-30:-" +sum);
		
		sum=x/y;
		System.out.println("50/30:-" +sum);
		
		sum=x%y;
		System.out.println("50%30:-" +sum);
		

	}

	}
	


